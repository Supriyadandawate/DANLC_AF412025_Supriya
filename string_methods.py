# 2. Remove duplicate characters
def remove_duplicates(string):
    seen = set()
    result = ""
    for char in string:
        if char not in seen:
            seen.add(char)
            result += char
    return result

input_string = "String and String Function"
result = remove_duplicates(input_string)
print(result)

"""# 3. Write a Python program to count Uppercase, Lowercase, special character and numeric values in a given string Input = “Hell0 W0rld ! 123 * # welcome to pYtHoN” Output: UpperCase : 5 LowerCase : 18 NumberCase : 5 SpecialCase : 1"""

# 3. Count Uppercase, Lowercase, special character and numeric values
def count_char_types(string):
    uppercase = 0
    lowercase = 0
    special_chars = 0
    numeric_values = 0

    for char in string:
        if char.isupper():
            uppercase += 1
        elif char.islower():
            lowercase += 1
        elif char.isdigit():
            numeric_values += 1
        else:
            special_chars += 1

    return uppercase, lowercase, special_chars, numeric_values

input_string = "Hell0 W0rld ! 123 * # welcome to pYtHoN"
uppercase, lowercase, special_chars, numeric_values = count_char_types(input_string)
print("UpperCase:", uppercase, "LowerCase:", lowercase, "NumberCase:", numeric_values, "SpecialCase:", special_chars)

